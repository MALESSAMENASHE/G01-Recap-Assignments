const add = (a, b) => {
    return a + b;
  };
  
  const sub = (a, b) => {
    return a - b;
  };
  const mul = (a, b) => {
    return a * b;
  };
  
  const div = (a, b) => {
    return a / b;
  };
  
  const square = (a) => {
    return Math.sqrt(a);
  };
  
  const power = (a, b) => {
    return Math.pow(a, b);
  };
  
  const mod = (a, b) => {
    return a % b;
  };
  